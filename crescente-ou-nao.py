# Faça um programa em Python que valide se as informações estão em ordem crescente.
primeiro=int(input("Informe o primeiro número inteiro: "))
segundo=int(input("Informe o segundo número inteiro: "))
terceiro=int(input("Informe o terceiro número inteiro: "))
if (primeiro < segundo) and (segundo < terceiro):
    print("crescente")
else:
    print("não está em ordem crescente")
